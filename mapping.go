package bitart

func join(bs ...uint8) string {
	if len(bs) == 0 {
		return ""
	}
	out := make([]byte, 0, 3*len(bs))
	for _, b := range bs {
		m := mapping[b]
		out = append(out, 0b_11100010, m[0], m[1])
	}
	return string(out)
}

